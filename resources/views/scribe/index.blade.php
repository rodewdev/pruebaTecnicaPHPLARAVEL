        <h1 id="introduction">Introducción</h1>
<p>API RESTful para gestión de usuarios y transacciones financieras con validaciones, seguridad y optimización de consultas.</p>
<aside>
    <strong>Base URL</strong>: <code>http://localhost</code>
</aside>
<h2 id="limites-y-restricciones">Límites y Restricciones</h2>
<ul>
<li><strong>Límite diario de transferencias</strong>: 5.000 USD por usuario emisor</li>
<li><strong>Monto mínimo de transferencia</strong>: 0,01 USD</li>
<li><strong>Monto máximo por transferencia</strong>: 5.000 USD</li>
<li>
<p><strong>Saldo mínimo</strong>: El usuario debe tener saldo suficiente para realizar la transferencia</p>
<p>Esta documentación proporciona toda la información necesaria para trabajar con nuestra API.</p>
<aside>A medida que desplazas la página, verás ejemplos de código para trabajar con la API en diferentes lenguajes de programación en el área oscura a la derecha (o como parte del contenido en dispositivos móviles).
Puedes cambiar el lenguaje utilizado con las pestañas en la parte superior derecha (o desde el menú de navegación en la parte superior izquierda en dispositivos móviles).</aside>
</li>
</ul>

        <h1 id="authenticating-requests">Autenticación de solicitudes</h1>
<p>Esta API utiliza Laravel Sanctum para la autenticación con tokens. Todas las rutas están protegidas excepto:</p>
<ul>
<li><code>POST /api/users</code> (registro de usuarios)</li>
<li><code>POST /api/login</code> (inicio de sesión)</li>
</ul>
<p>Para acceder a las rutas protegidas, debes incluir un token de autenticación en las solicitudes.</p>
<h2 id="autenticacion-con-laravel-sanctum">Autenticación con Laravel Sanctum</h2>
<p>En un entorno de producción, la API utilizaría Laravel Sanctum para la autenticación con tokens. El proceso sería el siguiente:</p>
<h3 id="1-obtener-un-token-de-autenticacion">1. Obtener un token de autenticación</h3>
<pre><code class="language-bash">POST /api/login
Content-Type: application/json

{
    "email": "juan@example.com",
    "password": "password"
}</code></pre>
<p>La respuesta incluiría el token de acceso:</p>
<pre><code class="language-json">{
    "success": true,
    "message": "Inicio de sesión exitoso",
    "data": {
        "token": "1|abcdefghijklmnopqrstuvwxyz",
        "user": {
            "id": 1,
            "name": "Juan Pérez",
            "email": "juan@example.com"
        }
    }
}</code></pre>
<h3 id="2-usar-el-token-en-las-solicitudes">2. Usar el token en las solicitudes</h3>
<pre><code class="language-bash">GET /api/users
Authorization: Bearer 1|abcdefghijklmnopqrstuvwxyz</code></pre>
